// Демонстрация работы операции %
public class Modulus {
    public static void main(String[] args) {
        int x = 42;
        double y = 42.25;
        System.out.println("Деление x по модулю 10 = " + x%10); // остаток от деления выведется в консоль
        System.out.println("Деление y по модулю 10 = " + y%10); // остаток от деления выведется в консоль
    }
}
