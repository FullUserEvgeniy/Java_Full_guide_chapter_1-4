// Демонстрация исполь зования динамической инициали зации
public class DinInit {
    public static void main(String[] args) {
        double a = 3.0, b = 4.0;
        double c = Math.sqrt(a * a + b *  b); // динамическая инициализация
        System.out.println("Длина гипотенузы равна " + c);
    }
}
